/**
 * Provides models for representing the data of the Bird Game application.
 * This package contains classes that define the core elements of the game,
 * handling the game state, player data, and interactions within the game environment.
 * 
 * This package includes the following models:
 *
 * - {@link birdgame.model.BirdModel}: Holds characteristics of individual birds in the game, encapsulating 
 *   properties like position, movement, and hit status.
 * 
 * - {@link birdgame.model.BirdFlockModel}: Holds the collection of Bird objects in a level.
 * 
 * - {@link birdgame.model.GameModel}: Holds the overall game state, including level information,
 *   time tracking, and background image paths.
 * 
 * - {@link birdgame.model.ScoreModel}: Holds the player's score throughout the game.
 * 
 * - {@link birdgame.model.WindowModel}: Serves as the data model for the game's user interface,
 *   linking the view components with the underlying game data.
 */
package birdgame.model;