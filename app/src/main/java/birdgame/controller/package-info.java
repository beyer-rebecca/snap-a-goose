/**
 * Provides controllers for managing different aspects of the Bird Game application.
 * This package contains a variety of controllers that handle interactions between
 * the game's model and view components. 
 * 
 * This package includes the following controllers:
 *
 * - {@link birdgame.controller.WindowController}: Manages the main application window,
 *   including navigation between different panels.
 * 
 * - {@link birdgame.controller.LoginController}: Manages the display of login and registration
 *   views, including handling user input for authentication.
 *
 * - {@link birdgame.controller.AuthenticationController}: Manages user authentication processes,
 *   including email verification and login validation.
 * 
 * - {@link birdgame.controller.GameController}: Orchestrates the main game loop and handles
 *   the overall game flow, including rendering and updating game elements.
 *
 * - {@link birdgame.controller.PlayerController}: Handles player interactions within the game,
 *   such as taking photos of birds and managing the player's score.
 *
 * - {@link birdgame.controller.ScoreController}: Manages the game's scoring system, including
 *   tracking and updating the player's score.
 *
 * - {@link birdgame.controller.HighscoreController}: Responsible for storing and retrieving
 *   highscores, as well as handling user authentication for score tracking.
 */
package birdgame.controller;
